int	ft_atoi(const char *str)
{
	int		i;
	int		s;
	long	r;
	long	r1;

	1 && (i = 0, r = 0, s = 1);
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == 43 || str[i] == 45)
	{
		(str[i] == 45) && (s = -1);
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		r1 = r;
		r = r * 10 + (str[i] - 48);
		if (r / 10 != r1 && s == 1)
			return (-1);
		if (r / 10 != r1 && s == -1)
			return (0);
		i++;
	}
	return (r * s);
}
