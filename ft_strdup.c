#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*p;
	size_t	d;
	size_t	i;

	i = 0;
	d = ft_strlen(s1);
	p = (char *)malloc(d + 1);
	if (!p)
		return (NULL);
	while (s1[i])
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
